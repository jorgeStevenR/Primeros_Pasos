
public class Declaracion_Operaciones {

	public static void main(String[] args) {
		
		int a = 5;
		
		int b;
		
		b = 7;
		
		int c = a + b;
		
		c++;
		
		
		System.out.println("El valor de c es de = " + c);
		
		c-=6;
		
		System.out.println("El valor de la variable C con un decremento de 6 es igual a : " + c);
		
		
		
		double x = 5;
		
		double y = 10;
		
		double z = y/x;
		
		System.out.println(z);


	}

}
